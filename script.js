let currentPage = 1;
const usersPerPage = 10;
let selectedRows = [];

function renderUsers() {
    const start = (currentPage - 1) * usersPerPage;
    const end = start + usersPerPage;
    const usersToDisplay = users.slice(start, end);

    const tableBody = document.getElementById("userTableBody");
    tableBody.innerHTML = "";

    usersToDisplay.forEach(user => {
        const row = document.createElement("tr");
        row.innerHTML = `
            <td>${user.id}</td>
            <td>${user.name}</td>
            <td>${user.email}</td>
            <td>${user.role}</td>

            <td>
                <button class="edit" onclick="editUser(${user.id})">Edit</button>
                <button class="delete" onclick="deleteUser(${user.id})">Delete</button>
            </td>
        `;
        if (selectedRows.includes(user.id)) {
            row.classList.add("selected");
        }

        tableBody.appendChild(row);
    });

    updatePagination();
}

function updatePagination() {
    const totalPages = Math.ceil(users.length / usersPerPage);
    document.getElementById("currentPage").innerText = `Page ${currentPage} of ${totalPages}`;
}

function goToPage(page) {
    currentPage = page;
    renderUsers();
}

function previousPage() {
    if (currentPage > 1) {
        currentPage--;
        renderUsers();
    }
}

function nextPage() {
    const totalPages = Math.ceil(users.length / usersPerPage);
    if (currentPage < totalPages) {
        currentPage++;
        renderUsers();
    }
}

function goToLastPage() {
    const totalPages = Math.ceil(users.length / usersPerPage);
    currentPage = totalPages;
    renderUsers();
}



let originalUsers = [...users]; // Keep a copy of the original data

function searchUsers() {
    const searchTerm = document.getElementById("searchInput").value.toLowerCase();
    const filteredUsers = originalUsers.filter(user =>
        Object.values(user).some(value => value.toString().toLowerCase().includes(searchTerm))
    );

    currentPage = 1;
    users = filteredUsers; // Update the users array with filtered data
    renderUsers();

    // Clear the search input after each search
    document.getElementById("searchInput").value = "";
}


